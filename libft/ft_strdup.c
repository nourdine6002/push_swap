#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	i;
	char	*arr;

	arr = malloc((ft_strlen(str) + 1) * sizeof(char));
	if (!arr)
		return (NULL);
	i = 0;
	while (str[i])
	{
		arr[i] = str[i];
		i++;
	}
	arr[i] = '\0';
	return (arr);
}
